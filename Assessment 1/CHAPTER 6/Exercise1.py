import math

#Number ceiling (rounds to the next integer)
a = 2.3
ceil_a = math.ceil(a)
print(f"Ceil of {a} is {ceil_a}")

#Floor of a number, rounded to the closest whole number
floor_a = math.floor(a)
print(f"Floor of {a} is {floor_a}")

#Factorial of a number
a = 5
factorial_a = math.factorial(a)
print(f"Factorial of {a} is {factorial_a}")

#The Value of pi
value_pi = math.pi
print(f"Value of Pi: {value_pi}")

#Square root of a number
a = 16
sqrt_a = math.sqrt(a)
print(f"Square root of {a} is {sqrt_a}")